        f2.setColumns(10);
        JLabel l3 = new JLabel("Contrase√±a: ");
        JTextField f3 = new JTextField();
        f3.setColumns(10);
        JLabel l4 = new JLabel("Direccion: ");
        JTextField f4 = new JTextField();
        f4.setColumns(10);
